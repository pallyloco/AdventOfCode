
import math

# ----------------------------------------------------------------------------
# globals
# ----------------------------------------------------------------------------
letters = list()
for i in range(26):
    letters.append(chr(ord('a') + i))

# ----------------------------------------------------------------------------
# Calculate the string from the number
# ----------------------------------------------------------------------------
def main(n):
    # ------------------------------------------------------------------------
    # get some info
    # ------------------------------------------------------------------------
    num_digits = math.ceil(n / 25) + 1
    p = n // 25

    # ------------------------------------------------------------------------
    # simple two 'digit' string
    # ------------------------------------------------------------------------
    if num_digits == 2:
        return f"a{letters[n]}"

    # ------------------------------------------------------------------------
    # if number is perfectly divisible by 25, then...
    # ------------------------------------------------------------------------
    if p*25 == n:
        s = "az" * int(num_digits / 2) + "a" * (num_digits % 2)
        return s

    # ----------------------------------------------------------------------------
    # n = (p-1)*25 + 2i (+/- 1)
    # the 1st three digits = (2i) = n-(p-1)*25
    # ----------------------------------------------------------------------------
    remainder = n - 25 * (p - 1)
    index = (remainder + 1) // 2
    s = "a" + letters[index] + "a"

    # ----------------------------------------------------------------------------
    # what is the rest of the string?
    # ----------------------------------------------------------------------------
    total = index * 2
    l1 = "z"
    l2 = "a"
    l3 = "b"
    l4 = "y"
    for i in range(p - 1):
        s += l1
        l2, l1 = l1, l2
        l3, l4 = l4, l3
        total = total + 25

    # if we are off by '1', then  change the last character
    if total - n == 1:
        s = s[:-1] + l3

    return s


# ----------------------------------------------------------------------------
# validation routines
# ----------------------------------------------------------------------------
def code(s):
    n = 0
    for i, j in zip(s[:-1], s[1:]):
        n = abs(ord(i) - ord(j)) + n
    return n


